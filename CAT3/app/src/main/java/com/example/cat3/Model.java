package com.example.cat3;

public class Model {
    String topName;
    int topImage;

    public Model(String topName, int topImage) {
        this.topName = topName;
        this.topImage = topImage;
    }

    public String getTopName() {
        return topName;
    }

    public int getTopImage() {
        return topImage;
    }

}
